abstract class Unit{
   constructor( 
      hp : number,
      damage : number,
      initiative : number,
      miss : number){
         this.hp = hp,
         this.damage = damage,
         this.initiative =initiative,
         this.miss = miss
      }
   hp : number;
   damage : number;
   initiative : number;
   miss : number;
   id : number;

}

